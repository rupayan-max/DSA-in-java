public class loop {
    public static void main(String[] args) {
        System.out.println("Loop in java");
        // Loop in java (For Loop)
        for (int myage = 0; myage <= 18; myage++) {
            System.out.println("Rupayan");
        }

        // Second Loop in java

        for (int countnumber = 1; countnumber <= 10; countnumber++) {
            System.out.println("Love Babbar");
        }

        // For Loop with 2 + increment

        for (int apple = 20; apple >= 1; apple = apple - 2) {
            System.out.println("Apple Count is --> " + apple);
        }
    }
}
