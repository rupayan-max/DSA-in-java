public class pattern {
    public static void main(String[] args) {
        System.out.println("Pattern print using nested loop");

        // Pattern print through nested loop

        // Basic Star pattern in java

        for (int a = 1; a <= 4; a++) {
            for (int b = 1; b <= 4; b++) {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
