public class dooloop {
    public static void main(String[] args) {
        System.out.println("Do While Loop In java ");

        // Do while Loop in java
        int marks = 1;
        do {
            System.out.println("I am In this loop");
            marks++;
        } while (marks <= 0); // condition False but this loop just run one time without condition checking
    }
}
