package pattern;

public class square {
    public static void main(String[] args) {
        System.out.println("Square Pattern Printing  in java ");
        for (int i = 1; i <= 4; i++) {
            for (int j = 1; j <= 5; j++) {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
