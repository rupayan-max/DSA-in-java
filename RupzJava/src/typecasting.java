public class typecasting {

    public static void main (){
        // Explicit Typecasting
        long  value1= 1325456;
        int value2=(int)value1;
        System.out.println(value2);

        //Implicit Typecasting
        byte num1 = 20;
        long num2 = num1;
        System.out.println(num2);
        
        // Character Warmup
        char hello= 'a';
        System.out.println((char)hello+2);
    }
}